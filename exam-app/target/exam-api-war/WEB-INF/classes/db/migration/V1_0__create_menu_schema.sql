
CREATE TABLE menu (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   title VARCHAR(255),
   description VARCHAR(255),
   CONSTRAINT pk_menu PRIMARY KEY (id)
);


CREATE TABLE meal (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   title VARCHAR(255),
   quantity INT,
   menu_id BIGINT,
   CONSTRAINT pk_meal PRIMARY KEY (id)
);

ALTER TABLE meal ADD CONSTRAINT FK_MEAL_ON_MENU FOREIGN KEY (menu_id) REFERENCES menu (id);




CREATE TABLE ordering (
  ordering_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   meal_order_id BIGINT,
   is_approved BOOLEAN,
   CONSTRAINT pk_ordering PRIMARY KEY (ordering_id)
);

CREATE TABLE meal_order (
  meal_order_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   meal_id BIGINT,
   quantity BIGINT,
   CONSTRAINT pk_mealorder PRIMARY KEY (meal_order_id)
);
